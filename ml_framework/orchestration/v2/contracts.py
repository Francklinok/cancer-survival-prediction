"""
contracts.py — Core data contracts for the  Pipeline Orchestration Engine.

These dataclasses are the vocabulary shared by all four layers (Analysis Engine,
Decision Engine, DAG Builder, Execution Engine). None of them contain business
logic — they are pure data carriers.

"""

from __future__ import annotations

import time
from dataclasses import dataclass, field
from enum import Enum
from typing import Any, Callable, Dict, FrozenSet, Optional


# ══════════════════════════════════════════════════════════════════════════════
# RECOMMENDATION — the normalized output of the Analysis Engine
# ══════════════════════════════════════════════════════════════════════════════


@dataclass(frozen=True)
class Recommendation:
    """
    Normalized decision signal produced by an Adapter from a business module's
    raw output. This is the ONLY vocabulary the Decision Engine is allowed to
    read — it never inspects a DataFrame or recomputes a statistic itself.

    Attributes
    ----------
    topic          : decision domain, e.g. "missing_values", "outliers", "scaling",
                     "encoding", "balancing", "feature_selection"
    required       : whether the corresponding processing step should run at all
    strategy       : the specific strategy chosen by the source module, if any
                     (e.g. "median", "SMOTE", "robust") — None if not applicable
    params         : extra parameters the strategy needs (free-form, module-specific)
    reason         : short human-readable justification (surfaced in reports/logs)
    confidence     : 0.0-1.0 — 1.0 for deterministic rule-based modules (the norm
                     in this framework today); lower for anything probabilistic
    source_module  : name of the business function that produced the raw signal
                     (traceability — always points back to an auditable function)
    raw            : the untouched raw output of the source module, kept for
                     full traceability and debugging (never re-parsed downstream)
    """

    topic: str
    required: bool
    strategy: Optional[str] = None
    params: Dict[str, Any] = field(default_factory=dict)
    reason: str = ""
    confidence: float = 1.0
    source_module: str = ""
    raw: Any = None

    def __post_init__(self) -> None:
        if not 0.0 <= self.confidence <= 1.0:
            raise ValueError(f"confidence must be in [0.0, 1.0], got {self.confidence}")
        if not self.topic:
            raise ValueError("Recommendation.topic must not be empty")


@dataclass(frozen=True)
class DatasetProfile:
    """
   Description of the dataset's shape and type.

        No guesswork: This is generated by a dedicated profiling module. 
        The engine itself never tries to guess this inline.

        Routing key: The problem_type acts as a routing key for the Decision Engine
        to figure out which modules are allowed to run (using the RouteByProblemType rule).

        Current status: Only "tabular" is supported right now.
    """

    problem_type: str  # "tabular" | "nlp" | "timeseries" | "vision" (future)
    n_rows: int
    n_columns: int
    dominant_dtype: str = ""
    metadata: Dict[str, Any] = field(default_factory=dict)


# ══════════════════════════════════════════════════════════════════════════════
# MODULE SPEC — declarative description of a business module, for the Registry
# ══════════════════════════════════════════════════════════════════════════════


CostHint = str  # "low" | "medium" | "high" — kept as a plain str to avoid enum churn


@dataclass(frozen=True)
class ModuleSpec:
    """
    Declarative description of one invocable unit of business logic.

    A ModuleSpec never contains business logic itself — `invoke` is a thin
    wrapper around an already-existing function in the framework (preprocessing,
    analysis, modeling, ...). This is what the Module Registry stores and what
    the DAG Builder reads to resolve dependencies via `inputs`/`outputs`.

    invoke(ctx) -> Any             : runs the wrapped business function
    adapter(raw_output) -> Recommendation : present only if produces_recommendation
    """

    name: str
    version: str
    capabilities: FrozenSet[str]
    inputs: FrozenSet[str]
    outputs: FrozenSet[str]
    invoke: Callable[[Any], Any]
    produces_recommendation: bool = False
    adapter: Optional[Callable[[Any], Recommendation]] = None
    cost_hint: CostHint = "medium"
    parallelizable: bool = True
    retryable: bool = True
    max_retries: int = 0

    def __post_init__(self) -> None:
        if self.produces_recommendation and self.adapter is None:
            raise ValueError(
                f"ModuleSpec '{self.name}': produces_recommendation=True requires an adapter."
            )
        if self.cost_hint not in ("low", "medium", "high"):
            raise ValueError(
                f"ModuleSpec '{self.name}': cost_hint must be low/medium/high, got '{self.cost_hint}'."
            )


# ══════════════════════════════════════════════════════════════════════════════
# EXECUTION PLAN — output of the Decision Engine
# ══════════════════════════════════════════════════════════════════════════════


@dataclass(frozen=True)
class ModuleInvocation:
    """
    One resolved call in an ExecutionPlan: which module, with which
    already-resolved parameters. The DAG Builder turns a list of these into
    a graph by cross-referencing their ModuleSpec.inputs/outputs.
    """

    module_name: str
    params: Dict[str, Any] = field(default_factory=dict)
    triggered_by: Optional[str] = None  # name of the Recommendation.topic that caused inclusion


@dataclass(frozen=True)
class ExecutionPlan:
    """
    Ordered list of modules the Decision Engine
    has decided must run for this dataset, plus the recommendations that
    justified each inclusion/exclusion (kept for audit trail).
    """

    invocations: tuple[ModuleInvocation, ...]
    dataset_profile: DatasetProfile
    recommendations: tuple[Recommendation, ...]
    skipped: tuple[str, ...] = field(default_factory=tuple)  # module names explicitly excluded


# ══════════════════════════════════════════════════════════════════════════════
# EVENTS — vocabulary published on the EventBus by the Execution Engine
# ══════════════════════════════════════════════════════════════════════════════


class EventType(str, Enum):
    MODULE_STARTED = "ModuleStarted"
    MODULE_COMPLETED = "ModuleCompleted"
    MODULE_SKIPPED = "ModuleSkipped"
    MODULE_FAILED = "ModuleFailed"
    ARTIFACT_CREATED = "ArtifactCreated"
    CACHE_HIT = "CacheHit"
    CACHE_MISS = "CacheMiss"
    RETRY_STARTED = "RetryStarted"
    PIPELINE_STARTED = "PipelineStarted"
    PIPELINE_COMPLETED = "PipelineCompleted"
    PIPELINE_FAILED = "PipelineFailed"


@dataclass(frozen=True)
class Event:
    """A single event published on the EventBus. Payload is event-specific."""

    type: EventType
    payload: Dict[str, Any] = field(default_factory=dict)
    timestamp: float = field(default_factory=time.time)
