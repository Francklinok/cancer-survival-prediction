"""
specs/missing.py — ModuleSpec for missing value imputation.

Business logic untouched: wraps missing_data_handling() verbatim, exactly as
MissingStep.run() does in orchestration/pipeline.py. The adapter reproduces
MissingStep.skip_if() as a Recommendation instead of an inline skip check —
"""

from __future__ import annotations

from ml_framework.orchestration.pipeline import PipelineContext
from ml_framework.orchestration.v2.contracts import ModuleSpec, Recommendation


def invoke(ctx: PipelineContext) -> None:
    from ml_framework.preprocessing.missing_value import missing_data_handling

    df_imp, report = missing_data_handling(
        ctx.df_work,
        strategy=ctx.config.data.missing_strategy,
    )
    ctx.df_work = df_imp
    ctx.artifacts["missing_imputation_report"] = report
    n_remaining = int(df_imp.isnull().sum().sum())
    print(f"  Missing values after imputation: {n_remaining}")


def adapt(raw_output: dict) -> Recommendation:
    """
    Here, `raw_output` is a lightweight probe dictionary `{"n_missing": int}` 
    generated by the Analysis Engine BEFORE the module runs. 

    It is not the final report from `missing_data_handling` (which only exists 
    after imputation). This approach mirrors the legacy `MissingStep.skip_if()`, 
    which checks for missing values up front rather than after the fact.
    """
    n_missing = int(raw_output.get("n_missing", 0))
    return Recommendation(
        topic="missing_values",
        required=n_missing > 0,
        strategy="mice" if n_missing > 0 else None,
        params={"n_missing": n_missing},
        reason=f"{n_missing} missing cell(s)" if n_missing > 0 else "no missing values",
        confidence=1.0,
        source_module="missing_data_handling",
        raw=raw_output,
    )


SPEC = ModuleSpec(
    name="missing",
    version="1.0.0",
    capabilities=frozenset({"tabular"}),
    inputs=frozenset({"df_work"}),
    outputs=frozenset({"df_work", "missing_imputation_report"}),
    invoke=invoke,
    produces_recommendation=True,
    adapter=adapt,
    cost_hint="medium",
)
